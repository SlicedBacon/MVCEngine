
public class View {
	
	private int horSize = 0; //horizontal size of the view
	private int vertSize = 0; //vertical size of the view
	
	public View()
	{
		
	}
	public int getHorSize() //returns the horizontal size of the view
	{
		return horSize;
	}
	public int getVertSize() //returns the vertical size of the view
	{
		return vertSize;
	}
	
}
